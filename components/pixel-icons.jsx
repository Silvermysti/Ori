function PixelIcon({ name, size = 20, color = "currentColor", className = "", style = {}, palette }) {
  const g = PX_ICONS[name];
  if (!g) return null;
  const rows = g.length,cols = g[0].length;
  const rects = [];
  for (let y = 0; y < rows; y++) {
    for (let x = 0; x < cols; x++) {
      const ch = g[y][x];
      if (ch === "." || ch === " ") continue;
      const fill = palette && palette[ch] ? palette[ch] : color;
      rects.push(<rect key={x + "_" + y} x={x} y={y} width="1.03" height="1.03" fill={fill} />);
    }
  }
  return (
    <svg className={"px-ico " + className} width={size} height={Math.round(size * rows / cols)}
    viewBox={`0 0 ${cols} ${rows}`} style={{ ...style, height: "30px" }} shapeRendering="crispEdges">
      {rects}
    </svg>);

}

function OriBuddy({ heat = 1, mood = "happy", size = 44, className = "", style = {} }) {
  const body = `var(--heat-${heat})`;
  const dark = "oklch(0.24 0.02 56)";
  const name = mood === "sleepy" ? "oriSleepy" : mood === "hot" ? "oriHot" : "oriHappy";
  return <PixelIcon name={name} size={size} palette={{ "#": body, o: dark, w: dark }}
  className={className} style={style} />;
}

